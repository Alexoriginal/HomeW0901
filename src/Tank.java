public class Tank {
  private String nameOfTank;
    private int weight;
    private int range;
    private int crew;

    public String getNameOfTank() {
        return nameOfTank;
    }

    public void setNameOfTank(String nameOfTank) {
        this.nameOfTank = "Оплот";
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getRange() {
        return range;
    }

    public void setRange(int range) {
        this.range = range;
    }

    public int getCrew() {
        return crew;
    }

    public void setCrew(int crew) {
        this.crew = crew;
    }
}
